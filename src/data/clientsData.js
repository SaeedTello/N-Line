// الحصول على قائمة الدول
export const getCountriesList = () => {
  return Object.keys(clientsData).map((key) => ({
    key,
    name: clientsData[key].country,
  }));
};

// الحصول على عملاء دولة معينة
export const getClientsByCountry = (countryKey) => {
  return clientsData[countryKey] || null;
};

// الحصول على عميل معين بواسطة ID
export const getClientById = (clientId) => {
  for (const country in clientsData) {
    const client = clientsData[country].clients.find(
      (c) => c.id === parseInt(clientId)
    );
    if (client) {
      return { ...client, country: clientsData[country].country };
    }
  }
  return null;
};
